// Создайте объект дуб oak с полем высота height и полем год year и методом расти grow. Пусть изначальная высота дуба будет 140 см, а год будет 2025. При вызове метода grow пусть дуб растет на 30 сантиметров, а год меняется на следующий.

// Запустите цикл и покажите, как дуб будет расти на протяжении 20 лет.

const oak = {
    height: 140,   // начальная высота в см
    year: 2025,    // начальный год
    grow() {
        this.height += 30;  // рост на 30 см
        this.year += 1;     // переход к следующему году
    }
};

// Симуляция роста на 20 лет
for (let i = 0; i < 20; i++) {
    oak.grow();
    console.log(`Год: ${oak.year}, Высота дуба: ${oak.height} см`);
}